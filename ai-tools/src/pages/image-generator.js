import React, { useState } from 'react';
import axios from 'axios';

export default function ImageGenerator() {
    const [userCommand, setUserCommand] = useState('');
    const [imageURL, setImageURL] = useState('');

    const onInputChangeHandler = (event) => {
        setUserCommand(event.target.value);
    };

    const onSubmitHandler = () => {
        // TODO: API Request
        axios
            .post('/api/image-generator', { userCommand })
            .then((response) => {
                console.log(response.data.imageURL);
                setImageURL(response.data.imageURL);
                setUserCommand('');
            })
            .catch((error) => {
                console.log(error);
            });
    };

    return (
        <div className="row justify-content-center">
            <div className="col-6">
                <div className="mb-3">
                    <h3 htmlFor="userCommand" className="form-label mb-3">
                        Enter Command to Generate Image:{' '}
                    </h3>
                    <textarea
                        value={userCommand}
                        id="userCommand"
                        className="form-control"
                        rows={4}
                        onChange={onInputChangeHandler}
                    />

                    <button
                        disabled={!userCommand || userCommand.length < 10}
                        className="btn btn-primary mt-3"
                        onClick={onSubmitHandler}>
                        Generate
                    </button>
                </div>

                {imageURL ? (
                    <div>
                        <h3>Image Generated by AI: </h3>
                        <img src={imageURL} placeholder="Generated By AI" />
                    </div>
                ) : null}
            </div>
        </div>
    );
}
