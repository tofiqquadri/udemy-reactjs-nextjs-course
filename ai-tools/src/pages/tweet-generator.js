import React, { useState } from 'react';
import axios from 'axios';

export default function TweetGenerator() {
    const [previousTweet, setPreviousTweet] = useState('');
    const [nextTweet, setNextTweet] = useState('');

    const onInputChangeHandler = (event) => {
        setPreviousTweet(event.target.value);
    };

    const onSubmitHandler = () => {
        // TODO: API Request
        axios
            .post('/api/tweet-generator', { tweet: previousTweet })
            .then((response) => {
                console.log(response.data);
                setNextTweet(response.data.data);
                setPreviousTweet('');
            })
            .catch((error) => {
                console.log(error);
            });
    };

    return (
        <div className="row justify-content-center">
            <div className="col-6">
                <div className="mb-3">
                    <h3 htmlFor="currentTweet" className="form-label mb-3">
                        Enter Previous Tweet:{' '}
                    </h3>
                    <textarea
                        value={previousTweet}
                        id="currentTweet"
                        className="form-control"
                        rows={4}
                        onChange={onInputChangeHandler}
                    />

                    <button
                        disabled={!previousTweet || previousTweet.length < 10}
                        className="btn btn-primary mt-3"
                        onClick={onSubmitHandler}>
                        Generate
                    </button>
                </div>

                {nextTweet ? (
                    <div>
                        <h3>Next Tweet Generated by AI: </h3>
                        <p>{nextTweet}</p>
                    </div>
                ) : null}
            </div>
        </div>
    );
}
